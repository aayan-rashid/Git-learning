
class datatypes
{
    public static void main(String a[])
    {
        
        int aa = 5;
        System.out.println(aa);
        boolean b = false;
        System.out.println(b);
        char c = 'c';
        System.out.println(c);
        int d = c;
        System.out.println(d);
        double dd = 6.98546;
        System.out.println(dd);
        float f = 6.5f;
        System.out.println(f);
        short s = 235;
        System.out.println(s);
        long l = 564722;
        System.out.println(l);
    }
}